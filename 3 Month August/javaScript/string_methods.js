//Return string length

const fullname = "Akshu";

console.log (fullname.length);

// toUpperCase() converts all char to upper case
 

console.log(fullname.toUpperCase());

//toLowerCase() converts all char to lower case

console.log(fullname.toLowerCase());

//trim() Removes extra space 

const Name = "   Priya   ";

console.log(Name.trim());

console.log(Name.trimStart());// removes start space

console.log(Name.trimEnd());//removes end  space

//charAt() return character of specific index 

console.log(fullname.charAt(2));//it gives index to space also

//indexOf()returns index of first accured letter 

console.log(fullname.indexOf("A"));
